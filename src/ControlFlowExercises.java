public class ControlFlowExercises {
    public static void main(String args[])
    {
        // a
        for(int i = 5; i <= 15; i++) {
            System.out.println(i);
        }

        // b
        for(int i = 100; i >= -10; i-=5) {
            System.out.println(i);
        }

        // c
        for(long i = 2; i < 1000000; i*=i) {
            System.out.println(i);
        }

//        for(long i = 2;)
    }
}
